create function tcu.fn_ProcessParameter
(	@ProcessId	smallint
,	@Parameter	varchar(20)
)
returns	varchar(4000)
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	01/25/2008
Purpose  :	Returns the specified value from the ProcessParameter table.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	declare	@value	varchar(4000)
	select	@value	= rtrim(Value)
	from	tcu.ProcessParameter
	where	ProcessId	= @ProcessId
	and		Parameter	= @Parameter
	return @value
end
GO
