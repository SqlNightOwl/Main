create function tcu.fn_RPadC
(	@value	sql_variant
,	@pad	smallint
,	@char	char(1)	)
returns varchar(4000)
as
/*
????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	05/18/2007
Purpose  :	Pads the provided string on the right with the pad character for the
			number of characters indicated.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
*/
begin
	return left(isnull(cast(@value as varchar(4000)), '') + replicate(@char, @pad), @pad)
end
GO
