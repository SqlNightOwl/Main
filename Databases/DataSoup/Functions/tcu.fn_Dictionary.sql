create function tcu.fn_Dictionary
(	@Application	varchar(125)
,	@Name			varchar(125)
)
returns	varchar(4000)
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	08/25/2007
Purpose  :	Returns the value from the tcuDictionary.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	declare	@value varchar(4000)
	select	@value	= rtrim(Value)
	from	tcu.Dictionary
	where	Application	= @Application
	and		Name		= @Name
	return @value
end
GO
