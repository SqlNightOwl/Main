create function tcu.fn_ZeroPad
(	@value	bigint
,	@pad	tinyint		)
returns varchar(255)
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	11/20/2005
Purpose  :	Zero pads the big integer to the desired length.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	set		@pad = case when isnull(@pad, 0) > 0 then @pad else len(cast(@value as varchar)) end
	return	right(replicate('0', @pad) + cast(@value as varchar), @pad)
end
GO
