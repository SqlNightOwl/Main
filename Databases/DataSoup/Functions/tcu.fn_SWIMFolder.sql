create function tcu.fn_SWIMFolder
(	@folderOffset	varchar(255)	)
returns varchar(255)
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	11/02/2007
Purpose  :	Appends the project folderOffset to the SQL Files and SWIM Files base
			folder and returns a standard UNC path\file.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	--	collect the sql files base folder path
	set	@folderOffset = tcu.fn_UNCFileSpec(
						tcu.fn_SQLFolder('\' +
						tcu.fn_Dictionary('OSI', 'SWIM Folder') + '\' + isnull(@folderOffset, '')))
	return @folderOffset
end
GO
