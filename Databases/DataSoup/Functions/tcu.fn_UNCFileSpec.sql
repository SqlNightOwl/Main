create function tcu.fn_UNCFileSpec
(	@fileSpec	varchar(255)	)
returns varchar(255)
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	02/19/2007
Purpose  :	Strips the file spec parameter to a standard UNC path\file.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	while charindex('\\', @fileSpec) > 0
		set @fileSpec = replace(@fileSpec, '\\', '\')
	return '\' + @fileSpec
end
GO
