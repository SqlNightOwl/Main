create function tcu.fn_ProcessNotificationTypes
(	@messageType	tinyint	)
returns varchar(200)
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Fijula Kuniyil
Created  :	01/07/2008
Purpose  :	Returns a semi-colon delimited list of Process Notification Types
			for the specified Notification Type Code.
History  :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	declare
		@pos	int
	,	@return	varchar(255)
	set @return	= null
	set	@pos	= 0
	while @pos < 4
	begin
		set	@return =	isnull(@return, '')
					+	case power(2, @pos) & @messageType
						when 0 then ''
						else case @pos
							 when 0 then 'Success'
							 when 1 then 'Failure'
							 when 2 then 'Information'
							 when 3 then 'Warning'
							 end + ';'
						end
		set	@pos = @pos + 1
	end
	return @return
end
GO