create function tcu.fn_LPad
(	@value	sql_variant
,	@pad	smallint	)
returns varchar(4000)
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	11/10/2006
Purpose  :	Pads the provided string on the left with the pad character for the
			number of characters indicated.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	return right(replicate(' ', @pad) + isnull(cast(@value as varchar(4000)), ''), @pad)
end
GO
