			c 2000-09 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	11/20/2005
Purpose  :	Returns the first day of the month for the date value provided.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	set	@dateValue = convert(char(10), isnull(@dateValue, getdate()), 101);
	return dateadd(day, -day(@dateValue) + 1, @dateValue);
end;
GO
