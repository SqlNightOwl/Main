create function tcu.fn_FileExists
(	@fileName	sysname	)
returns int
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	01/12/2007
Purpose  :	The extended procedure xp_FileExist doesn't honor SET NOCOUNT ON and
			thus function will suppress any rowcount it generates.
			number of characters indicated.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	declare @exists	int	-- exists = 1, not exists = 0
	execute master.sys.xp_FileExist @fileName, @exists output
	return @exists
end
GO
