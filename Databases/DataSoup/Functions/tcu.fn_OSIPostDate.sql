create function tcu.fn_OSIPostDate()
returns datetime
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-10 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	08/08/2008
Purpose  :	Returns the current post date from OSI.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	declare	@postDate datetime;
	select	@postDate = cast(postDate as datetime)
	from	openquery(OSI,'
			select	BankOptionValue as PostDate
			from	osiBank.BankOption
			where	BankOptionCd = ''PDAT''');
	return @postDate;
end;
GO
