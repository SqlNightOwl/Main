create function tcu.fn_FTPFolder
(	@folderOffset	varchar(255)	)
returns varchar(255)
as
/*
????????????????????????????????????????????????????????????????????????????????
			c 2000-08 ? Texans Credit Union ? All rights reserved.
????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	04/26/2007
Purpose  :	Appends the project folderOffset to the FTP Files base folder and
			returns a standard UNC path\file.
History	 :
   Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin
	set	@folderOffset =	tcu.fn_UNCFileSpec(
						tcu.fn_Dictionary('All Applications', 'FTP Files Folder')
						+ '\' + isnull(@folderOffset, ''))
	return @folderOffset
end
GO
