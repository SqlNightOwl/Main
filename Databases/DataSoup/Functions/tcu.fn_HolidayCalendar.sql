????????????????????????????????????????????????????????????????????????????????
Developer:	Paul Hunter
Created  :	01/02/2007
Purpose  :	Caclulates the desired holiday date for the year provided.
History  :
  Date		Developer		Description
??????????	??????????????	????????????????????????????????????????????????????
????????????????????????????????????????????????????????????????????????????????
*/
begin

	insert	@results
	select	Holiday
		,	HolidayOn = tcu.fn_HolidayDate(@Year, MonthOccurs, Frequency, DayOccurs, IsFloating)
	from	tcu.Holiday;

	return;
end;
GO
